
    create table adult_user (
        id bigint generated by default as identity,
        user_id bigint unique,
        wallet_id bigint unique,
        cpf varchar(255) not null unique,
        email varchar(255) not null unique,
        job varchar(255),
        primary key (id)
    );

    create table adult_user_dependents (
        adult_user_id bigint not null,
        dependents_id bigint not null unique
    );

    create table adult_user_tasks (
        adult_user_id bigint not null,
        tasks_id bigint not null unique
    );

    create table child_task (
        status tinyint not null check (status between 0 and 3),
        value float(53) not null,
        adult_user_id bigint,
        child_user_id bigint,
        id bigint generated by default as identity,
        description varchar(255) not null,
        primary key (id)
    );

    create table child_user (
        family tinyint check (family between 0 and 3),
        birthday timestamp(6) not null,
        guardian_id bigint,
        id bigint generated by default as identity,
        user_id bigint unique,
        wallet_id bigint unique,
        primary key (id)
    );

    create table child_user_objectives (
        child_user_id bigint not null,
        objectives_id bigint not null unique
    );

    create table child_user_tasks (
        child_user_id bigint not null,
