    );

    create table objective (
        current_amount float(53) not null,
        objective_value float(53) not null,
        child_user_id bigint,
        id bigint generated by default as identity,
        description varchar(255) not null,
        primary key (id)
    );

    create table release (
        type tinyint not null check (type between 0 and 1),
        value float(53) not null,
        date timestamp(6) not null,
        id bigint generated by default as identity,
        wallet_id bigint,
        description varchar(255),
        primary key (id)
    );

    create table user (
        id bigint generated by default as identity,
        name varchar(255) not null,
        nickname varchar(255) not null,
        password varchar(255) not null,
        primary key (id)
    );

    create table wallet (
        money float(53) not null,
        id bigint generated by default as identity,
        primary key (id)
    );

    create table wallet_extract (
        extract_id bigint not null unique,
        wallet_id bigint not null
    );

